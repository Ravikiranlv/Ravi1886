package java_program_in_softcopy_Nots;

public class repeet_number {
	public static void main(String[] args) {

		int a = 10;
		for (int i = 1; i < 8; i++) {
			if (i < 4) {
				System.out.print(i);
			} else {
				System.out.print(4);
			}
		}
		for (int i = 5; i <= a; i++) {
			System.out.print(i);
		}
	}
}
